class Vehicle:
    def __init__(self):
        self.health = 100
        self.fuel = 20
        self.gas_tank_size = 20
        self.passengers = []
    def get_health(self):
        return self.health
    def reduce_health(self, reduction):
        self.health -= reduction
    def use_fuel(self, reduction):
        self.fuel -= reduction
    def __str__(self):
        return f"Vechicle Health: {self.health}, Gas: {self.fuel}"
    def add_passenger(self, passenger):
        self.passengers.append(passenger)
    def show_passengers(self):
        for passenger in self.passengers:
            print(passenger.get_name())
    def clear_passengers(self):
        self.passengers.clear()

class Passenger:
    
    def __init__ (self, name):
        self.name = name
        self.status = "Healthy"
        self.hunger = 100
        self.phone_battery = 100
    
    def get_name(self):
        return self.name
    def set_name(self, new_name):
        self.name = new_name
    def get_status(self):
        return self.status
    def set_status(self, new_status):
        self.status = new_status
    def get_hunger(self):
        return self.hunger
    def get_phone_battery(self):
        return self.phone_battery
    def reduce_hunger(self, reduction):
        self.hunger -= reduction
    def increase_hunger(self, increase):
        self.hunger += increase
    def reduce_phone_battery(self, reduction):
        self.phone_battery -= reduction

    def __str__(self):
        return f"Name: {self.name}, Status: {self.status}, Hunger: {self.hunger} Phone Battery: {self.phone_battery}"

class Supplies:
    def __init__(self):
        self.snacks = 0
        self.medicine = 0
        self.money = 500
    def get_snacks(self):
        return self.snacks
    def add_snacks(self, amount):
        self.snacks += amount
    def get_money(self):
        return self.money
    def get_medicine(self):
        return self.medicine
    def use_snack(self):
        if self.snacks > 0:
            self.snacks -= 1
    def use_medicine(self):
        if self.medicine > 0:
            self.medicine -= 1
    def spend_money(self):
        if self.money > 0:
            self.money -= 10
    def __str__(self):
        return f"Supplies - Snacks: {self.snacks}, Medicine: {self.medicine}, Money: {self.money}"

