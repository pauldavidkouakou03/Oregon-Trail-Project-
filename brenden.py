        for rank, (scr, line) in enumerate(top_10, 1):
            # Parse the old line to rebuild it nicely
            date, driver, survivors, miles, money, old_score = [x.strip() for x in line.split("|")]
            f.write(f"{rank:2}   | {date} | {driver:15} | {survivors:9} | {miles:6} | {money:10} | {scr:,.0f}\n")
