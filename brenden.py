    entry = {
        "date": date_str,
        "driver": car.passengers[0].get_name(),   # driver is always index 0
        "survivors": living,
        "miles": car.get_miles_driven(),
        "money_left": supplies.money,
        "car_health": car.get_health(),
        "score": score
    }
    
    # Read existing entries (if any)
    entries = []
    if os.path.exists(HALL_OF_FAME_FILE):
        with open(HALL_OF_FAME_FILE, "r", encoding="utf-8") as f:
            lines = f.readlines()
            # Skip the header lines (first 4 lines)
            for line in lines[4:]:
                if line.strip() and not line.startswith("-"):
                    parts = line.strip().split("|")
                    if len(parts) >= 6:
                        try:
                            old_score = int(parts[5].strip())
                            entries.append((old_score, line.strip()))
                        except:
                            pass  # corrupted line â†’ ignore
    
    # Add new entry
    entries.append((score, f"{date_str} | {car.passengers[0].get_name():15} | {living}/4 survivors | {car.get_miles_driven():4} mi | ${supplies.money} left | {score:,} pts"))
    
    # Sort by score descending and take top 10
    entries.sort(reverse=True, key=lambda x: x[0])
    top_10 = entries[:10]
    
    # Write back
    with open(HALL_OF_FAME_FILE, "w", encoding="utf-8") as f:
        f.write("ğŸ† MODERN OREGON TRAIL HALL OF FAME ğŸ†\n")
        f.write("="*80 + "\n")
